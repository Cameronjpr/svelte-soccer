<h1>How to play GameweekGurus</h1>

<section class="p-4">
	<p>
		<strong>GameweekGurus</strong> is a free to play fantasy football game where you can predict the
		outcome of Premier League fixtures.
	</p>

	<p>
		Each week, you pick one team you think will win – but there's a catch: you can only pick each
		team
		<strong>twice per season</strong>.
	</p>

	<p>
		If your team wins, you gain 1 point. If they draw, you gain nothing – but if they lose, you lose
		a point!
	</p>

	<p>The player with the most points at the end of the season wins. Good luck!</p>
</section>

