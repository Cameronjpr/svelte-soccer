<h1>How to play</h1>

<section class="p-4 flex flex-col gap-8">
	<p>
		<mark class="p-1 font-semibold mr-1">1. Pick a team:</mark>Each week, you pick one team you
		think will win – but there's a catch: you can only pick each team
		<strong>twice per season</strong>.
	</p>

	<p>
		<mark class="p-1 font-semibold mr-1">2. Get points if they win:</mark>If your team wins, you
		gain 3 points. If they draw, you gain nothing – but if they lose, you lose 1 point!
	</p>

	<p>
		<mark class="p-1 font-semibold mr-1">3. Climb the rankings:</mark>The player with the most
		points at the end of the season wins. Good luck!
	</p>
</section>
