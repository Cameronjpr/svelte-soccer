<h1>How to play PremPredictor</h1>

<p>
	<strong>PremPredictor</strong> is a free to play fantasy football game where you can predict the outcome
	of Premier League fixtures.
</p>

<p>
	Each week, you pick one team you think will win – but there's a catch: you can only pick each team
	<strong>twice per season</strong>.
</p>

<p>
	If your team wins, you gain 1 point. If they draw, you gain nothing – but if they lose, you lose a
	point!
</p>

<p>
	Correctly picking a winning team in 3 consecutive weeks will earn you a <strong
		>streak multiplier</strong
	>, which means you earn 2 points for a win!
</p>

<p>
	There's also a reward for picking the underdog: if you pick a team in the <strong
		>relegation zone</strong
	> (at the start of the gameweek), you'll earn 2 points for a win – and this can also be multiplied
	by a streak for a total of 4 points!
</p>

